/* Tables created after initialization */

int scrambler_sequence[SCRAMBLER_TABLE_LENGTH];
FComplex scrambler_table[SCRAMBLER_TABLE_LENGTH];
FComplex scrambler_train_table[SCRAMBLER_TABLE_LENGTH];

/* 
 * 
 * By pre-computing the scrambler values it should speed thing up a bit.
 *
 */
void iterate_data_scrambler_sequence( int *state )
{
	int    temp;
	temp = 0;
	
	if( (*state)&0x0001 ) temp++;
	if( (*state)&0x0010 ) temp++;
	
	*state = (*state)>>1;
	
	if( temp&1 ) *state |= 0x0100;
}
void build_data_scrambler_array(void)
{
	int i;
	int state;
	
	state = 0x1FF;
	
	for( i = 0; i < SCRAMBLER_TABLE_LENGTH ; i++ )
	{
		scrambler_sequence[i] = state&0x07;
		iterate_data_scrambler_sequence( &state );
		iterate_data_scrambler_sequence( &state );
		iterate_data_scrambler_sequence( &state );		
	}	
}
		
void build_scrambler_table(void)
{
	int i;

	build_data_scrambler_array();
	
	for( i = 0; i < SCRAMBLER_TABLE_LENGTH; i++ )
	{
		scrambler_table[i]            = scrambler_phase[scrambler_sequence[i]];
		scrambler_train_table[i].real = scrambler_table[i].real*0.01;
		scrambler_train_table[i].imag = scrambler_table[i].imag*0.01;
	}
	for( i = 0; i < PREAMBLE_LENGTH; i++ )
	{
		rx_preamble_lookup[i].real = tx_preamble_lookup[i].real*0.01;
		rx_preamble_lookup[i].imag = tx_preamble_lookup[i].imag*0.01;
	}
}
