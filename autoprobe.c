void tx_auto_probe(void)
{
	int i,m;

	tx_count = 0;
	m = sm_params->tx_mode;
	
	for( i=0; i < 32; i++ ) tx_bpsk( autoprobe_table[m][i] );
	for( i=0; i < 32; i++ ) tx_bpsk( autoprobe_table[m][i] );
	for( i=0; i < 32; i++ ) tx_bpsk( autoprobe_table[m][i] );
	for( i=0; i < 32; i++ ) tx_bpsk( autoprobe_table[m][i] );
}
int rx_autoprobe_correlate( float *input )
{
	int i,j;
	float max,temp;
	int   max_index;
	
	/* Correlate against known auto baud sequence */
	max       = 0;
	max_index = 0;

	for( i=0; i<32; i++ )
	{
		temp = 0;
		for( j=0; j<32; j++ )
		{
			if( autoprobe_table[i][j])
				temp  -= input[j];
			else
				temp  += input[j];			
		}
		/* See if we have a more likely format */
		
		if( temp > max )
		{
			max = temp;
			max_index = i;
		}
	}
	return max_index;	
}
int process_rx_autoprobe( float *input )
{
	float sum[32];
	int   mode[2];
	int   i;
	static int last_mode;

	/* Accumulate the metric */
	for( i=0; i<32; i++ )
	{
		sum[i] = input[i]+input[i+64];
	}
	mode[0] = rx_autoprobe_correlate( sum );

	for( i=0; i<32; i++ )
	{
		sum[i] = +input[i+32]+input[i+96];
	}
	mode[1] = rx_autoprobe_correlate( sum );
	
	if( mode[0] == mode[1] )
	{
		last_mode = mode[0];
	}
	if( sm_params->auto_baud )
		set_rx_mode( last_mode );
	return last_mode;	
} 